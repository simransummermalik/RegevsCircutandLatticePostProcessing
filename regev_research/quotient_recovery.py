def exact_quotient_deduplicated_recovery(
    family: RootedBaseFamily,
    samples: Sequence[Sequence[int]],
    modulus: int,
    *,
    verified_l0_directions: Sequence[Sequence[int]] = (),
    scale: int | Fraction | None = None,
    noise_bound: int | Fraction | None = None,
    lll_delta: Fraction = Fraction(3, 4),
    budget: RecoveryBudget = RecoveryBudget(),
    ordering: str = "exact_embedding_norm",
    relation_lift: RelationLift | None = None,
    root_blind: bool = False,
) -> RecoveryResult:
    """Intervention B: de-duplicate candidates by their exact coset modulo ``U``.

    ``verified_l0_directions`` generates the integer row lattice ``U`` exactly.
    Its Hermite basis is not saturated or rationally projected.  In root-blind
    mode only ``U=0`` is permitted, since that ablation cannot verify an L0
    direction.
    """

    if root_blind and verified_l0_directions:
        raise ValueError("root_blind quotient recovery requires the zero U lattice")
    if root_blind:
        quotient_helper = None
        quotient_key_function: QuotientKey = _root_blind_identity_key
        quotient_invariants = _zero_u_invariants(len(family.pairs))
    else:
        quotient_helper = build_exact_unsaturated_quotient_key(
            family, verified_l0_directions
        )
        quotient_key_function = quotient_helper
        quotient_invariants = quotient_helper.invariants
    embedding, reduction, start = _build_and_reduce(
        family,
        samples,
        modulus,
        scale=scale,
        noise_bound=noise_bound,
        lll_delta=lll_delta,
    )
    result = enumerate_reduced_basis_combinations(
        family,
        embedding,
        reduction,
        budget=budget,
        strategy=(
            "B_exact_unsaturated_quotient_deduplication_root_blind"
            if root_blind
            else "B_exact_unsaturated_quotient_deduplication"
        ),
        ordering=ordering,
        relation_lift=relation_lift,
        quotient_key=quotient_key_function,
        root_blind=root_blind,
        reductions=1,
        started_at=start,
    )
    return replace(
        result,
        quotient_invariants=quotient_invariants,
        interpretation=(
            f"{result.interpretation} Candidate keys are exact Hermite-reduced "
            "cosets modulo the supplied unsaturated integer U; Smith invariants "
            "are reported but never used to enlarge U."
        ),
    )


def _rectangular_exact_row_lll(
    rows: Sequence[Sequence[int]],
    original_transform_rows: Sequence[Sequence[int]],
    original_basis: Sequence[Sequence[int]],
    *,
    delta: Fraction,
) -> tuple[tuple[tuple[int, ...], ...], tuple[tuple[int, ...], ...], str]:
    """LLL rows and compose its square row transform back to ``original_basis``."""

    try:
        from sympy import Matrix, Rational
    except ImportError as exc:  # pragma: no cover
        raise RuntimeError("SymPy is required for exact rectangular row LLL") from exc
    row_matrix = Matrix(rows)
    transform_matrix = Matrix(original_transform_rows)
    if row_matrix.rows > row_matrix.cols:
        raise ValueError("row LLL requires no more rows than ambient columns")
    if transform_matrix.rows != row_matrix.rows:
        raise ValueError("row and composed-transform counts disagree")
    try:
        reduced, local_transform = row_matrix.lll_transform(
            delta=Rational(delta.numerator, delta.denominator)
        )
    except Exception as exc:  # SymPy exposes backend-specific rectangular errors.
        raise RuntimeError(
            "exact rectangular SymPy row-LLL is unavailable for this basis; "
            "no projection or fake deflation was substituted"
        ) from exc
    composed = local_transform * transform_matrix
    original = Matrix(original_basis)
    if composed * original != reduced:
        raise ArithmeticError("rectangular LLL transform did not compose to the original basis")
    return (
        tuple(tuple(int(value) for value in row) for row in reduced.tolist()),
        tuple(tuple(int(value) for value in row) for row in composed.tolist()),
        "sympy.Matrix.lll_transform rectangular exact row basis",
    )


def exact_augmented_basis_deflation_from_reduction(
    family: RootedBaseFamily,
    embedding: AugmentedLattice,
    reduction: LLLReduction,
    *,
    lll_delta: Fraction = Fraction(3, 4),
    max_deletions: int | None = None,
    initial_reductions: int = 0,
    started_at: float | None = None,
) -> AugmentedBasisDeflationResult:
    """Delete verified-L0 *basis rows* and re-LLL the exact chosen section.

    A deleted row is an actual element of the current free row basis, hence a
    primitive generator of that free module.  Deleting it realizes the chosen
    section spanned by the remaining basis rows.  The remaining rectangular
    integer basis is row-LLL reduced, and every local unimodular transform is
    composed back to the original augmented basis.  No row is divided,
    projected, saturated, or removed merely through rational-span membership.
    """

    if not isinstance(family, RootedBaseFamily):
        raise TypeError("augmented deflation requires a RootedBaseFamily")
    if embedding.dimension != len(family.pairs):
        raise ValueError("embedding dimension does not match the rooted family")
    lattice_dimension = embedding.dimension + embedding.sample_count
    if len(reduction.reduced_rows) != lattice_dimension:
        raise ValueError("initial reduction must be a full augmented row basis")
    if len(reduction.transform_rows) != lattice_dimension:
        raise ValueError("initial transform must be a full augmented row basis")
    if isinstance(lll_delta, bool) or not isinstance(lll_delta, (int, Fraction)):
        raise TypeError("lll_delta must be an int or fractions.Fraction")
    lll_delta = Fraction(lll_delta)
    if not Fraction(1, 4) < lll_delta < 1:
        raise ValueError("lll_delta must lie strictly between 1/4 and 1")
    if max_deletions is None:
        deletion_cap = lattice_dimension
    else:
        deletion_cap = _as_int(max_deletions, "max_deletions")
        if deletion_cap < 0:
            raise ValueError("max_deletions cannot be negative")
    reductions = _as_int(initial_reductions, "initial_reductions")
    if reductions < 0:
        raise ValueError("initial_reductions cannot be negative")

    start = perf_counter() if started_at is None else started_at
    original_basis = embedding.integer_row_basis
    current_rows = tuple(reduction.reduced_rows)
    current_transform = tuple(reduction.transform_rows)
    if any(
        tuple(
            sum(current_transform[i][k] * original_basis[k][j] for k in range(lattice_dimension))
            for j in range(lattice_dimension)
        )
        != current_rows[i]
        for i in range(lattice_dimension)
    ):
        raise ArithmeticError("initial reduction transform does not map the original basis")

    deleted: list[DeletedAugmentedBasisRow] = []
    relation_u = _ExactGeneratedL0Lattice(embedding.dimension)
    projected_checks = 0
    modular_checks = 0
    classifications = 0
    backends = [reduction.backend]
    factor_relation = None
    factor_pair = None
    termination_reason = ""
    peak_memory = (
        _matrix_memory_estimate(original_basis)
        + _matrix_memory_estimate(current_rows)
        + _matrix_memory_estimate(current_transform)
    )

    while current_rows:
        actionable = None
        for row_position in sorted(
            range(len(current_rows)),
            key=lambda i: (sum(value * value for value in current_rows[i]), i),
        ):
            row = current_rows[row_position]
            transform_row = current_transform[row_position]
            projected = tuple(transform_row[i] for i in range(embedding.dimension))
            expected_prefix = tuple(
                embedding.clearing_factor * value for value in projected
            )
            if row[: embedding.dimension] != expected_prefix:
                raise ArithmeticError("row projection disagrees with its original transform")
            if not any(projected):
                continue
            projected_checks += 1
            relation, _ = _canonical_sign(projected)
            modular_checks += 1
            if modular_product(family.N, family.bases, relation) != 1:
                continue
            classification = classify_square_relation(family, relation)
            classifications += 1
            if classification["class"] == "L_minus_L0":
                factor_relation = relation
                factor_pair = _factor_pair(family.N, classification["factor"])
                actionable = ("factor", row_position, relation, classification)
                break
            if classification["class"] == "L0":
                actionable = ("delete", row_position, relation, classification)
                break

        if actionable is None:
            termination_reason = "no_verified_projected_relation_basis_row"
            break
        action, row_position, relation, classification = actionable
        if action == "factor":
            termination_reason = "factor_found_after_exact_augmented_row_deflation"
            break
        if len(deleted) >= deletion_cap:
            termination_reason = "frozen_max_deletions_reached"
            break

        deleted_row = current_rows[row_position]
        deleted_transform = current_transform[row_position]
        # Re-verify immediately at the destructive boundary.
        repeated = classify_square_relation(family, relation)
        classifications += 1
        if repeated["class"] != "L0":
            raise ArithmeticError("attempted to delete a row not verified in L0")
        relation_u.add(relation)
        deleted.append(
            DeletedAugmentedBasisRow(
                deletion_index=len(deleted),
                row_position_before_deletion=row_position,
                cleared_augmented_row=deleted_row,
                original_embedding_coefficients=deleted_transform,
                projected_l0_relation=relation,
                root_product=int(classification["root_product"]),
                cleared_squared_norm=sum(value * value for value in deleted_row),
                primitive_in_current_free_row_basis=True,
            )
        )
        remaining_rows = tuple(
            row for i, row in enumerate(current_rows) if i != row_position
        )
        remaining_transform = tuple(
            row for i, row in enumerate(current_transform) if i != row_position
        )
        if not remaining_rows:
            current_rows = ()
            current_transform = ()
            termination_reason = "all_free_basis_rows_deleted"
            break
        current_rows, current_transform, backend = _rectangular_exact_row_lll(
            remaining_rows,
            remaining_transform,
            original_basis,
            delta=lll_delta,
        )
        reductions += 1
        backends.append(backend)
        peak_memory = max(
            peak_memory,
            _matrix_memory_estimate(original_basis)
            + _matrix_memory_estimate(current_rows)
            + _matrix_memory_estimate(current_transform)
            + _matrix_memory_estimate(relation_u.basis),
        )

    quotient_helper = build_exact_unsaturated_quotient_key(family, relation_u.basis)
    return AugmentedBasisDeflationResult(
        strategy="B_stronger_exact_augmented_free_basis_row_deflation",
        embedding=embedding,
        deleted_rows=tuple(deleted),
        relation_u=relation_u.basis,
        quotient_invariants=quotient_helper.invariants,
        factor_relation=factor_relation,
        factor_pair=factor_pair,
        final_reduced_rows=current_rows,
        final_transform_rows=current_transform,
        termination_reason=termination_reason,
        resources=AugmentedDeflationResources(
            projected_rows_checked=projected_checks,
            modular_relation_checks=modular_checks,
            root_classifications=classifications,
            reductions=reductions,
            deleted_rows=len(deleted),
            runtime_seconds=perf_counter() - start,
            peak_memory_estimate_bytes=peak_memory,
            reduction_backends=tuple(backends),
        ),
        interpretation=(
            "Exact quotient of the current free augmented row module by each "
            "deleted primitive basis generator, represented by the chosen section "
            "spanned by remaining rows; transforms are composed to the original "
            "integer embedding. This is not orthogonal projection, rational-span "
            "deletion, saturation, or a quotient-lattice metric claim."
        ),
    )


