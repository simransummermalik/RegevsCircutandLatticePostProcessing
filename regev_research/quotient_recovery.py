def _normalize_sample_rows_for_ldar(
    rows: Sequence[Sequence[int]],
    *,
    dimension: int,
    modulus: int,
    name: str,
) -> tuple[tuple[int, ...], ...]:
    normalized = tuple(
        tuple(_as_int(value, f"{name}[{i}][{j}]") for j, value in enumerate(row))
        for i, row in enumerate(rows)
    )
    for i, row in enumerate(normalized):
        if len(row) != dimension:
            raise ValueError(f"{name}[{i}] must have dimension {dimension}")
        if any(value < 0 or value >= modulus for value in row):
            raise ValueError(f"{name}[{i}] has a coordinate outside [0, modulus)")
    return normalized


def _aggregate_ldar_resources(stages: Sequence[LDARStage]) -> LDARTotalResources:
    resources = [stage.resources for stage in stages]
    return LDARTotalResources(
        stages=len(stages),
        nodes_generated=sum(row.nodes_generated for row in resources),
        nodes_visited=sum(row.nodes_visited for row in resources),
        modular_relation_checks=sum(row.modular_relation_checks for row in resources),
        root_classifications=sum(row.root_classifications for row in resources),
        suppressed_l0_nodes=sum(row.suppressed_l0_nodes for row in resources),
        reductions=sum(row.reductions for row in resources),
        runtime_seconds=sum(row.runtime_seconds for row in resources),
        peak_memory_estimate_bytes=max(
            (row.peak_memory_estimate_bytes for row in resources), default=0
        ),
    )


def sequential_ldar_recovery(
    family: RootedBaseFamily,
    initial_samples: Sequence[Sequence[int]],
    fresh_sample_pool: Sequence[Sequence[int]],
    modulus: int,
    *,
    frozen_max_sample_count: int,
    fresh_sample_seed: int,
    samples_per_acquisition: int = 1,
    max_deflation_rounds_per_sample_count: int = 8,
    scale: int | Fraction | None = None,
    noise_bound: int | Fraction | None = None,
    lll_delta: Fraction = Fraction(3, 4),
    budget: RecoveryBudget = RecoveryBudget(),
    root_blind: bool = False,
) -> LDARResult:
    """Complete sequential A+B+C recovery with genuine sample acquisition.

    At each accumulated sample count the method:

    1. builds the exact unsaturated quotient ``Z^d/U``;
    2. LLL-reduces and norm-orders bounded row combinations (A);
    3. de-duplicates by the exact quotient key (B);
    4. verifies candidates and adds only stored-root-certified L0 rows to ``U``
       while suppressing their exact integer span (C);
    5. repeats at the same sample count after ``U`` changes, then consumes
       never-before-used rows from the separately supplied fresh pool.

    The method accepts no factors, orders, or relation oracle.  The fresh pool
    is sampled without replacement.  Equal measured values at two distinct
    pool indices are still genuine independent acquisitions; no existing row
    is copied or bootstrapped.

    With ``root_blind=True``, steps 3--4 use only ``U=0`` and candidates are
    checked solely for membership in ``L``.  The ablation never evaluates a
    stored root, classifies L0, suppresses a direction, or extracts a factor.
    """

    if not isinstance(family, RootedBaseFamily):
        raise TypeError("LDAR requires an immutable RootedBaseFamily")
    modulus = _as_int(modulus, "modulus")
    if modulus <= 1:
        raise ValueError("modulus must exceed one")
    dimension = len(family.pairs)
    initial = _normalize_sample_rows_for_ldar(
        initial_samples, dimension=dimension, modulus=modulus, name="initial_samples"
    )
    fresh = _normalize_sample_rows_for_ldar(
        fresh_sample_pool,
        dimension=dimension,
        modulus=modulus,
        name="fresh_sample_pool",
    )
    if len(initial) < dimension + 4:
        raise ValueError("initial_samples must contain at least d + 4 rows")
    frozen_max_sample_count = _as_int(
        frozen_max_sample_count, "frozen_max_sample_count"
    )
    if frozen_max_sample_count < len(initial):
        raise ValueError("frozen_max_sample_count cannot be below the initial count")
    required_fresh = frozen_max_sample_count - len(initial)
    if len(fresh) < required_fresh:
        raise ValueError("fresh_sample_pool is too small for the frozen sample maximum")
    samples_per_acquisition = _as_int(
        samples_per_acquisition, "samples_per_acquisition"
    )
    if samples_per_acquisition <= 0:
        raise ValueError("samples_per_acquisition must be positive")
    max_deflation_rounds_per_sample_count = _as_int(
        max_deflation_rounds_per_sample_count,
        "max_deflation_rounds_per_sample_count",
    )
    if max_deflation_rounds_per_sample_count <= 0:
        raise ValueError("max_deflation_rounds_per_sample_count must be positive")

    rng = np.random.default_rng(_as_int(fresh_sample_seed, "fresh_sample_seed"))
    fresh_order = tuple(int(value) for value in rng.permutation(len(fresh)))
    next_fresh = 0
    selected_fresh: list[int] = []
    accumulated = list(initial)
    pending_new_indices: tuple[int, ...] = ()
    stages: list[LDARStage] = []
    global_u = _ExactGeneratedL0Lattice(dimension)
    factor_pair = None
    termination_reason = ""
    deflation_round = 0

    while True:
        if root_blind:
            quotient_before_helper = None
            quotient_before_invariants = _zero_u_invariants(dimension)
            quotient_key_function: QuotientKey = _root_blind_identity_key
        else:
            quotient_before_helper = build_exact_unsaturated_quotient_key(
                family, global_u.basis
            )
            quotient_before_invariants = quotient_before_helper.invariants
            quotient_key_function = quotient_before_helper
        embedding, reduction, start = _build_and_reduce(
            family,
            accumulated,
            modulus,
            scale=scale,
            noise_bound=noise_bound,
            lll_delta=lll_delta,
        )
        recovery = enumerate_reduced_basis_combinations(
            family,
            embedding,
            reduction,
            budget=budget,
            strategy=(
                "LDAR_root_blind_L_membership_only"
                if root_blind
                else "LDAR_A_norm_B_exact_quotient_C_exact_L0"
            ),
            ordering="exact_embedding_norm",
            adaptive_l0_suppression=not root_blind,
            seed_l0_directions=() if root_blind else global_u.basis,
            quotient_key=quotient_key_function,
            root_blind=root_blind,
            reductions=1,
            started_at=start,
        )

        new_l0: list[tuple[int, ...]] = []
        if not root_blind:
            if quotient_before_helper is None:  # pragma: no cover - type narrowing.
                raise AssertionError("root-aware LDAR lost its quotient helper")
            for candidate in recovery.candidates:
                if candidate.relation_class != "L0":
                    continue
                if not quotient_before_helper.quotient.is_zero(
                    quotient_before_helper.quotient.reduce(candidate.relation)
                ):
                    if global_u.add(candidate.relation):
                        new_l0.append(candidate.relation)
        if root_blind:
            quotient_after_invariants = _zero_u_invariants(dimension)
        else:
            quotient_after_invariants = build_exact_unsaturated_quotient_key(
                family, global_u.basis
            ).invariants
        u_changed = (
            quotient_before_invariants.hnf_basis
            != quotient_after_invariants.hnf_basis
        )
        round_limit_reached = (
            u_changed
            and deflation_round + 1 >= max_deflation_rounds_per_sample_count
        )
        factor_pair = recovery.factor_pair
        stages.append(
            LDARStage(
                stage_index=len(stages),
                sample_count=len(accumulated),
                deflation_round_at_sample_count=deflation_round,
                newly_acquired_sample_indices=pending_new_indices,
                quotient_before=quotient_before_invariants,
                quotient_after=quotient_after_invariants,
                u_changed=u_changed,
                deflation_round_limit_reached=round_limit_reached,
                newly_verified_l0_directions=tuple(new_l0),
                factor_pair=factor_pair,
                candidate_relations=tuple(
                    candidate.relation for candidate in recovery.candidates
                ),
                candidate_classes=tuple(
                    candidate.relation_class for candidate in recovery.candidates
                ),
                resources=recovery.resources,
            )
        )
        pending_new_indices = ()

        if factor_pair is not None:
            termination_reason = "factor_found_from_stored_roots"
            break
        if u_changed and deflation_round + 1 < max_deflation_rounds_per_sample_count:
            deflation_round += 1
            continue

        hit_round_cap = round_limit_reached
        if len(accumulated) >= frozen_max_sample_count:
            termination_reason = (
                "frozen_max_sample_count_reached_after_deflation_round_cap"
                if hit_round_cap
                else "frozen_max_sample_count_reached"
            )
            break

        take = min(
            samples_per_acquisition,
            frozen_max_sample_count - len(accumulated),
        )
        indices = fresh_order[next_fresh : next_fresh + take]
        if len(indices) != take:
            raise ArithmeticError("validated fresh sample pool was unexpectedly exhausted")
        next_fresh += take
        selected_fresh.extend(indices)
        accumulated.extend(fresh[index_value] for index_value in indices)
        pending_new_indices = tuple(indices)
        deflation_round = 0

    final_quotient_invariants = (
        _zero_u_invariants(dimension)
        if root_blind
        else build_exact_unsaturated_quotient_key(family, global_u.basis).invariants
    )
    return LDARResult(
        method=(
            "LDAR_root_blind_ablation"
            if root_blind
            else "LDAR_exact_A_plus_B_plus_C_plus_genuine_new_samples"
        ),
        root_blind=root_blind,
        initial_sample_count=len(initial),
        final_sample_count=len(accumulated),
        frozen_max_sample_count=frozen_max_sample_count,
        samples_per_acquisition=samples_per_acquisition,
        selected_fresh_sample_indices=tuple(selected_fresh),
        stages=tuple(stages),
        final_u=() if root_blind else global_u.basis,
        final_quotient=final_quotient_invariants,
        factor_pair=factor_pair,
        termination_reason=termination_reason,
        total_resources=_aggregate_ldar_resources(stages),
    )


def standard_lll_enumeration_baseline(
    family: RootedBaseFamily,
    samples: Sequence[Sequence[int]],
    modulus: int,
    *,
    scale: int | Fraction | None = None,
    noise_bound: int | Fraction | None = None,
    lll_delta: Fraction = Fraction(3, 4),
    budget: RecoveryBudget = RecoveryBudget(),
) -> RecoveryResult:
    """Matched baseline: standard LLL and shell/lex bounded enumeration."""

    embedding, reduction, start = _build_and_reduce(
        family,
        samples,
        modulus,
        scale=scale,
        noise_bound=noise_bound,
        lll_delta=lll_delta,
    )
    return enumerate_reduced_basis_combinations(
        family,
        embedding,
        reduction,
        budget=budget,
        strategy="baseline_standard_LLL_enumeration",
        ordering="shell_lexicographic",
        reductions=1,
        started_at=start,
    )


def random_extra_samples_baseline(
    family: RootedBaseFamily,
    samples: Sequence[Sequence[int]],
    extra_sample_pool: Sequence[Sequence[int]],
    modulus: int,
    *,
    extra_count: int,
    seed: int,
    scale: int | Fraction | None = None,
    noise_bound: int | Fraction | None = None,
    lll_delta: Fraction = Fraction(3, 4),
    budget: RecoveryBudget = RecoveryBudget(),
) -> RecoveryResult:
    """Matched baseline using genuinely additional samples chosen at random.

    The caller must supply an independently generated ``extra_sample_pool``.
    This function never bootstraps existing rows and therefore cannot disguise
    duplicated data as additional information.
    """

    extra_count = _as_int(extra_count, "extra_count")
    if extra_count <= 0 or extra_count > len(extra_sample_pool):
        raise ValueError("extra_count must lie in [1, len(extra_sample_pool)]")
    rng = np.random.default_rng(_as_int(seed, "seed"))
    indices = tuple(
        sorted(int(value) for value in rng.choice(len(extra_sample_pool), extra_count, replace=False))
    )
    combined = [*samples, *(extra_sample_pool[i] for i in indices)]
    embedding, reduction, start = _build_and_reduce(
        family,
        combined,
        modulus,
        scale=scale,
        noise_bound=noise_bound,
        lll_delta=lll_delta,
    )
    result = enumerate_reduced_basis_combinations(
        family,
        embedding,
        reduction,
        budget=budget,
        strategy="baseline_random_genuine_extra_samples",
        ordering="shell_lexicographic",
        reductions=1,
        extra_samples_used=extra_count,
        started_at=start,
    )
    return replace(result, selected_extra_sample_indices=indices)


def _verified_fpylll_bkz_reduction(
    embedding: AugmentedLattice, block_size: int
) -> LLLReduction | None:
    """Return a transform-verified fpylll BKZ result, or ``None`` if unavailable."""

    try:
        from fpylll import BKZ, IntegerMatrix
    except ImportError:
        return None
    dimension = len(embedding.integer_row_basis)
    basis = IntegerMatrix.from_matrix([list(row) for row in embedding.integer_row_basis])
    transform = IntegerMatrix.identity(dimension)
    try:  # API details vary between fpylll releases; never accept an unverified transform.
        BKZ.reduction(basis, BKZ.Param(block_size=block_size), U=transform)
    except (TypeError, ValueError, RuntimeError):
        return None
    reduced_rows = tuple(
        tuple(int(basis[i, j]) for j in range(dimension)) for i in range(dimension)
    )
    transform_rows = tuple(
        tuple(int(transform[i, j]) for j in range(dimension)) for i in range(dimension)
    )
    for i in range(dimension):
        reconstructed = tuple(
            sum(transform_rows[i][k] * embedding.integer_row_basis[k][j] for k in range(dimension))
            for j in range(dimension)
        )
        if reconstructed != reduced_rows[i]:
            return None
    return LLLReduction(
        reduced_rows=reduced_rows,
        transform_rows=transform_rows,
        delta=Fraction(99, 100),
        backend=f"fpylll.BKZ(block_size={block_size}); transform verified",
    )


def bkz_or_deterministic_surrogate_baseline(
    family: RootedBaseFamily,
    samples: Sequence[Sequence[int]],
    modulus: int,
    *,
    block_size: int = 6,
    scale: int | Fraction | None = None,
    noise_bound: int | Fraction | None = None,
    budget: RecoveryBudget = RecoveryBudget(),
) -> RecoveryResult:
    """BKZ when verified fpylll support exists; otherwise a named surrogate."""

    block_size = _as_int(block_size, "block_size")
    if block_size < 2:
        raise ValueError("block_size must be at least two")
    start = perf_counter()
    embedding = build_augmented_lattice(
        samples,
        modulus,
        scale=scale,
        noise_bound=noise_bound,
        require_regev_sample_count=True,
    )
    if embedding.dimension != len(family.pairs):
        raise ValueError("sample dimension does not match the rooted family")
    reduction = _verified_fpylll_bkz_reduction(embedding, block_size)
    if reduction is not None:
        strategy = "baseline_verified_fpylll_BKZ_plus_bounded_enumeration"
        interpretation = "Verified fpylll BKZ transform followed by the common node-bounded search."
        search_budget = budget
    else:
        reduction = lll_reduce_augmented_lattice(embedding, delta=Fraction(99, 100))
        reduction = replace(
            reduction,
            backend=(
                "deterministic strong SymPy LLL(delta=99/100) + bounded "
                f"block-combination search(block_size={block_size}); NOT BKZ"
            ),
        )
        strategy = "baseline_deterministic_strong_LLL_block_surrogate_NOT_BKZ"
        interpretation = (
            "No transform-verified BKZ backend was available; this is a deterministic "
            "strong-LLL/block-search surrogate and is not reported as BKZ."
        )
        search_budget = replace(
            budget,
            max_support=min(budget.max_support or block_size, block_size),
        )
    result = enumerate_reduced_basis_combinations(
        family,
        embedding,
        reduction,
        budget=search_budget,
        strategy=strategy,
        ordering="exact_embedding_norm",
        reductions=1,
        started_at=start,
    )
    return replace(result, interpretation=f"{result.interpretation} {interpretation}")


def matched_cost_recovery_suite(
    family: RootedBaseFamily,
    samples: Sequence[Sequence[int]],
    extra_sample_pool: Sequence[Sequence[int]],
    modulus: int,
    *,
    extra_count: int,
    seed: int,
    scale: int | Fraction | None = None,
    noise_bound: int | Fraction | None = None,
    budget: RecoveryBudget = RecoveryBudget(),
    seed_l0_directions: Sequence[Sequence[int]] = (),
    block_size: int = 6,
    relation_lift: RelationLift | None = None,
    quotient_key: QuotientKey | None = None,
) -> MatchedCostRecoveryComparison:
    """Run A, B, C, and three baselines with the same combination-node cap.

    The common cap matches the dominant bounded-enumeration work.  Reduction
    runtime and memory can differ (especially when genuine samples are added),
    so those costs are reported rather than declared equal.
    """

    standard = standard_lll_enumeration_baseline(
        family, samples, modulus, scale=scale, noise_bound=noise_bound, budget=budget
    )
    intervention_a = short_combination_recovery(
        family,
        samples,
        modulus,
        scale=scale,
        noise_bound=noise_bound,
        budget=budget,
        relation_lift=relation_lift,
        quotient_key=quotient_key,
    )
    intervention_b = exact_quotient_deduplicated_recovery(
        family,
        samples,
        modulus,
        verified_l0_directions=seed_l0_directions,
        scale=scale,
        noise_bound=noise_bound,
        budget=budget,
        relation_lift=relation_lift,
    )
    intervention_c = adaptive_l0_suppression_recovery(
        family,
        samples,
        modulus,
        scale=scale,
        noise_bound=noise_bound,
        budget=budget,
        seed_l0_directions=seed_l0_directions,
        relation_lift=relation_lift,
        quotient_key=quotient_key,
    )
    extra = random_extra_samples_baseline(
        family,
        samples,
        extra_sample_pool,
        modulus,
        extra_count=extra_count,
        seed=seed,
        scale=scale,
        noise_bound=noise_bound,
        budget=budget,
    )
    bkz_or_surrogate = bkz_or_deterministic_surrogate_baseline(
        family,
        samples,
        modulus,
        block_size=block_size,
        scale=scale,
        noise_bound=noise_bound,
        budget=budget,
    )
    return MatchedCostRecoveryComparison(
        budget=budget,
        matching_rule=(
            "All six arms use the same max_nodes, coefficient bound, and requested "
            "row cap. Reduction count/backend, generated ordering nodes, runtime, and "
            "memory are reported separately; they are not assumed equal."
        ),
        results=(
            standard,
            intervention_a,
            intervention_b,
            intervention_c,
            extra,
            bkz_or_surrogate,
        ),
    )
