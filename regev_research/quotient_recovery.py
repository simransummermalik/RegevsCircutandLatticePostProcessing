    """Hashable-key adapter backed by one exact :class:`IntegerQuotient`."""

    quotient: "IntegerQuotient"
    invariants: QuotientInvariants

    def __call__(
        self,
        relation: tuple[int, ...],
        _dynamically_known_l0: tuple[tuple[int, ...], ...],
    ) -> Hashable:
        # The helper is intentionally frozen to the verified U used to build
        # it.  LDAR rebuilds it after U changes; it never silently changes the
        # equivalence relation halfway through one de-duplication pass.
        return self.quotient.reduce(relation)


