"""Cost-audited recovery interventions around the augmented Regev lattice.

This module deliberately separates three concerns:

* :mod:`regev_research.lattice` constructs and LLL-reduces the exact augmented
  lattice;
* this module enumerates bounded combinations of reduced rows and verifies
  their projected relations against an immutable :class:`RootedBaseFamily`;
* an optional quotient/deflation module may supply a relation lift or a
  canonical quotient key through the hooks below.  No import of such a module
  is required here.

Intervention A orders the same finite coefficient search by the exact norm of
the resulting cleared embedding vector.  Intervention B de-duplicates by exact
Hermite-reduced cosets of the unsaturated integer lattice generated by verified
``L0`` rows.  Intervention C adaptively suppresses
the exact integer lattice generated by relations already verified to be in
``L0``.  C never suppresses a vector merely because it lies in the *rational*
span of known ``L0`` directions: doing so could erase a nontrivial torsion
coset.  These are bounded experimental searches, not a proof of exhaustive
SVP/CVP recovery.

The BKZ comparison uses ``fpylll`` only when a transformation matrix can be
obtained and verified.  Otherwise its result is labelled, in both strategy and
backend fields, as a deterministic strong-LLL plus bounded block-combination
surrogate and is never called BKZ.
"""

