@dataclass(frozen=True)
class RecoveryBudget:
    """Common bounded-search budget used for matched comparisons."""

    enumeration_rows: int = 6
    coefficient_bound: int = 2
    max_support: int | None = None
    max_nodes: int = 20_000
    max_materialized_nodes: int = 1_000_000

    def __post_init__(self) -> None:
        if self.enumeration_rows <= 0:
            raise ValueError("enumeration_rows must be positive")
        if self.coefficient_bound <= 0:
            raise ValueError("coefficient_bound must be positive")
        if self.max_support is not None and self.max_support <= 0:
            raise ValueError("max_support must be positive when supplied")
        if self.max_nodes <= 0:
            raise ValueError("max_nodes must be positive")
        if self.max_materialized_nodes <= 0:
            raise ValueError("max_materialized_nodes must be positive")


