def _as_int(value: object, name: str) -> int:
    if isinstance(value, bool):
        raise TypeError(f"{name} must be an integer, not bool")
    try:
        return int(index(value))
    except TypeError as exc:
        raise TypeError(f"{name} must be an integer") from exc


def _canonical_sign(vector: Sequence[int]) -> tuple[tuple[int, ...], int]:
    normalized = tuple(int(value) for value in vector)
    if not any(normalized):
        return normalized, 1
    sign = 1 if next(value for value in normalized if value) > 0 else -1
    return tuple(sign * value for value in normalized), sign


def _factor_pair(N: int, factor: int | None) -> tuple[int, int] | None:
    if factor is None:
        return None
    if not 1 < factor < N or N % factor:
        raise ArithmeticError("relation classifier returned an invalid factor")
    return tuple(sorted((factor, N // factor)))


