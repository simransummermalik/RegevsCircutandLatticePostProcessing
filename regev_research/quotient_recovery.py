def build_exact_unsaturated_quotient_key(
    family: RootedBaseFamily,
    verified_l0_directions: Sequence[Sequence[int]],
) -> ExactUnsaturatedQuotientKey:
    """Build an exact canonical key modulo the *integer* lattice ``U``.

    Every nonzero row is verified by :func:`build_l0_quotient`.  An empty
    direction list produces the zero sublattice, so the key is simply the
    canonical ambient integer vector.  Smith torsion and the saturation index
    are reported for diagnosis only: neither is used to enlarge ``U``.
    """

    if not isinstance(family, RootedBaseFamily):
        raise TypeError("quotient-key construction requires a RootedBaseFamily")
    # Local import keeps this recovery module usable while quotient.py evolves
    # and avoids any import cycle through optional experiment orchestration.
    from .quotient import build_l0_quotient

    quotient = build_l0_quotient(family, verified_l0_directions)
    return ExactUnsaturatedQuotientKey(
        quotient=quotient,
        invariants=_quotient_invariants(quotient),
    )


def _integer_bytes(value: int) -> int:
    # Approximate a Python integer by its object header plus magnitude bytes.
    return 28 + max(1, (abs(int(value)).bit_length() + 7) // 8)


def _matrix_memory_estimate(rows: Sequence[Sequence[int]]) -> int:
    return 64 + sum(
        64 + 8 * len(row) + sum(_integer_bytes(value) for value in row)
        for row in rows
    )


def _state_memory_estimate(
    embedding: AugmentedLattice,
    reduction: LLLReduction,
    *,
    materialized_coefficients: int,
    coefficient_width: int,
    seen_relations: int,
    relation_dimension: int,
    candidate_count: int,
    l0_basis: Sequence[Sequence[int]],
) -> int:
    matrices = (
        _matrix_memory_estimate(embedding.integer_row_basis)
        + _matrix_memory_estimate(reduction.reduced_rows)
        + _matrix_memory_estimate(reduction.transform_rows)
        + _matrix_memory_estimate(l0_basis)
    )
    coefficient_storage = materialized_coefficients * (
        64 + 8 * coefficient_width + 28 * coefficient_width
    )
    relation_storage = (seen_relations + candidate_count) * (
        96 + 8 * relation_dimension + 28 * relation_dimension
    )
    return matrices + coefficient_storage + relation_storage


class _ExactGeneratedL0Lattice:
    """Exact integer span of verified L0 vectors, maintained in column HNF."""

    def __init__(self, dimension: int) -> None:
        self.dimension = dimension
        self._basis: tuple[tuple[int, ...], ...] = ()

    @property
    def basis(self) -> tuple[tuple[int, ...], ...]:
        return self._basis

    def contains(self, vector: Sequence[int]) -> bool:
        z = tuple(int(value) for value in vector)
        if len(z) != self.dimension:
            raise ValueError("L0 membership vector has the wrong dimension")
        if not self._basis:
            return not any(z)
        try:
            from sympy import Matrix
        except ImportError as exc:  # pragma: no cover
            raise RuntimeError("SymPy is required for exact L0 suppression") from exc
        columns = Matrix(self._basis).T
        try:
            solution, parameters = columns.gauss_jordan_solve(Matrix(z))
        except ValueError:
            return False
        if parameters.rows:
            raise ArithmeticError("HNF basis unexpectedly had dependent columns")
        return all(value.q == 1 for value in solution)

    def add(self, vector: Sequence[int]) -> bool:
        z, _ = _canonical_sign(vector)
        if len(z) != self.dimension or not any(z):
            raise ValueError("an L0 generator must be a nonzero vector of dimension d")
        if self.contains(z):
            return False
        try:
            from sympy import Matrix
            from sympy.matrices.normalforms import hermite_normal_form
        except ImportError as exc:  # pragma: no cover
            raise RuntimeError("SymPy is required for exact L0 suppression") from exc
        generators = [*self._basis, z]
        hnf = hermite_normal_form(Matrix(generators).T)
        basis = tuple(
            tuple(int(hnf[row, column]) for row in range(self.dimension))
            for column in range(hnf.cols)
        )
        if any(not any(row) for row in basis):
            raise ArithmeticError("HNF returned a zero L0 generator")
        self._basis = basis
        return True


def exact_generated_l0_membership(
    generators: Sequence[Sequence[int]], vector: Sequence[int]
) -> bool:
    """Public audit helper for exact *integer*, not rational-span, membership."""

    z = tuple(int(value) for value in vector)
    lattice = _ExactGeneratedL0Lattice(len(z))
    for generator in generators:
        lattice.add(generator)
    return lattice.contains(z)


def _coefficient_vectors(
    width: int, bound: int, max_support: int
) -> Iterable[tuple[int, ...]]:
    """Yield one sign-canonical representative in expanding L-infinity shells."""

    for radius in range(1, bound + 1):
        for coefficients in product(range(-radius, radius + 1), repeat=width):
            if max(abs(value) for value in coefficients) != radius:
                continue
            if sum(value != 0 for value in coefficients) > max_support:
                continue
            first = next((value for value in coefficients if value), 0)
            if first > 0:
                yield tuple(int(value) for value in coefficients)


def _combine_rows(
    coefficients: Sequence[int], rows: Sequence[Sequence[int]]
) -> tuple[int, ...]:
    return tuple(
        sum(coefficient * rows[i][j] for i, coefficient in enumerate(coefficients))
        for j in range(len(rows[0]))
    )


def _ordered_coefficient_nodes(
    rows: Sequence[Sequence[int]],
    budget: RecoveryBudget,
    ordering: str,
) -> tuple[Iterable[tuple[int, ...]], int, int]:
    width = len(rows)
    max_support = min(width, budget.max_support or width)
    stream = _coefficient_vectors(width, budget.coefficient_bound, max_support)
    if ordering == "shell_lexicographic":
        return stream, 0, max_support
    if ordering != "exact_embedding_norm":
        raise ValueError("ordering must be shell_lexicographic or exact_embedding_norm")

    nodes = []
    for coefficients in stream:
        if len(nodes) >= budget.max_materialized_nodes:
            raise ValueError(
                "norm ordering would exceed max_materialized_nodes; reduce rows, "
                "coefficient_bound, or max_support"
            )
        nodes.append(coefficients)
    nodes.sort(key=lambda coefficients: (
        sum(value * value for value in _combine_rows(coefficients, rows)),
        max(abs(value) for value in coefficients),
        coefficients,
    ))
    return nodes, len(nodes), max_support


def enumerate_reduced_basis_combinations(
    family: RootedBaseFamily,
    embedding: AugmentedLattice,
    reduction: LLLReduction,
    *,
    budget: RecoveryBudget = RecoveryBudget(),
    strategy: str = "A_short_combination_enumeration",
    ordering: str = "exact_embedding_norm",
    adaptive_l0_suppression: bool = False,
    seed_l0_directions: Sequence[Sequence[int]] = (),
    relation_lift: RelationLift | None = None,
    quotient_key: QuotientKey | None = None,
    root_blind: bool = False,
    reductions: int = 0,
    extra_samples_used: int = 0,
    stop_on_factor: bool = False,
    max_stored_candidates: int = 512,
    started_at: float | None = None,
) -> RecoveryResult:
    """Enumerate bounded combinations of an already reduced augmented basis.

    ``relation_lift`` maps the raw first-``d`` transform coefficients to an
    original relation.  It is the composition seam for exact quotient or
    deflation code.  ``quotient_key`` may provide a canonical de-duplication
    key using the current exact HNF basis of discovered ``L0`` directions.
    Both hooks are optional, and neither is trusted: every lifted vector is
    re-verified in ``L``.  Unless ``root_blind=True``, it is then classified
    with the roots stored in ``family``.  The root-blind ablation performs no
    root product, GCD, L0 classification, or suppression.
    """

    if not isinstance(family, RootedBaseFamily):
        raise TypeError("recovery requires an immutable RootedBaseFamily")
    if embedding.dimension != len(family.pairs):
        raise ValueError("embedding dimension does not match the rooted family")
    lattice_dimension = embedding.dimension + embedding.sample_count
    if len(reduction.reduced_rows) != lattice_dimension:
        raise ValueError("reduction row count does not match the embedding")
    if len(reduction.transform_rows) != lattice_dimension:
        raise ValueError("reduction transform count does not match the embedding")
    if max_stored_candidates <= 0:
        raise ValueError("max_stored_candidates must be positive")
    if root_blind and adaptive_l0_suppression:
        raise ValueError("root_blind recovery cannot suppress or classify L0")
    if root_blind and seed_l0_directions:
        raise ValueError("root_blind recovery cannot accept verified L0 directions")

    start = perf_counter() if started_at is None else started_at
    row_count = min(budget.enumeration_rows, lattice_dimension)
    selected_indices = sorted(
        range(lattice_dimension),
        key=lambda i: (
            sum(value * value for value in reduction.reduced_rows[i]),
            i,
        ),
    )[:row_count]
    reduced_rows = tuple(reduction.reduced_rows[i] for i in selected_indices)
    transform_rows = tuple(reduction.transform_rows[i] for i in selected_indices)
    coefficient_nodes, materialized_count, max_support = _ordered_coefficient_nodes(
        reduced_rows, budget, ordering
    )

    l0_lattice = _ExactGeneratedL0Lattice(embedding.dimension)
    for i, direction in enumerate(seed_l0_directions):
        relation, _ = _canonical_sign(direction)
        if len(relation) != embedding.dimension or not any(relation):
            raise ValueError(f"seed_l0_directions[{i}] has invalid dimension or is zero")
        classification = classify_square_relation(family, relation)
        if classification["class"] != "L0":
            raise ValueError(f"seed_l0_directions[{i}] is not a verified L0 relation")
        l0_lattice.add(relation)

    candidates: list[RecoveryCandidate] = []
    seen_keys: set[Hashable] = set()
    nodes_generated = materialized_count
    nodes_visited = 0
    duplicates = 0
    zero_projections = 0
    modular_checks = 0
    verified = 0
    classifications = 0
    suppressed = 0
    discovered = len(l0_lattice.basis)
    factor_pair = None
    exhausted = False

    for coefficients in coefficient_nodes:
        if materialized_count == 0:
            nodes_generated += 1
        if nodes_visited >= budget.max_nodes:
            exhausted = True
            break
        nodes_visited += 1
        cleared_vector = _combine_rows(coefficients, reduced_rows)
        projected = tuple(
            sum(
                coefficient * transform_rows[i][coordinate]
                for i, coefficient in enumerate(coefficients)
            )
            for coordinate in range(embedding.dimension)
        )
        expected_prefix = tuple(
            embedding.clearing_factor * value for value in projected
        )
        if cleared_vector[: embedding.dimension] != expected_prefix:
            raise ArithmeticError("reduced rows and transform disagree on relation projection")
        if not any(projected):
            zero_projections += 1
            continue
        projected, sign = _canonical_sign(projected)
        coefficients = tuple(sign * value for value in coefficients)
        cleared_vector = tuple(sign * value for value in cleared_vector)

        if relation_lift is None:
            relation = projected
        else:
            relation = tuple(_as_int(value, "lifted relation coordinate") for value in relation_lift(projected))
            if len(relation) != embedding.dimension:
                raise ValueError("relation_lift returned the wrong dimension")
            relation, lift_sign = _canonical_sign(relation)
            if not any(relation):
                zero_projections += 1
                continue
            if lift_sign < 0:
                coefficients = tuple(-value for value in coefficients)
                cleared_vector = tuple(-value for value in cleared_vector)

        known_l0 = l0_lattice.basis
        key = (
            quotient_key(relation, known_l0)
            if quotient_key is not None
            else relation
        )
        try:
            duplicate = key in seen_keys
        except TypeError as exc:
            raise TypeError("quotient_key must return a hashable value") from exc
        if duplicate:
            duplicates += 1
            continue
        seen_keys.add(key)

        if adaptive_l0_suppression and l0_lattice.contains(relation):
            suppressed += 1
            continue
        modular_checks += 1
        if modular_product(family.N, family.bases, relation) != 1:
            continue
        verified += 1
        if root_blind:
            relation_class = "L_unclassified_root_blind"
            root_product = None
            relation_factor_pair = None
        else:
            classification = classify_square_relation(family, relation)
            classifications += 1
            relation_class = str(classification["class"])
            root_product = int(classification["root_product"])
            relation_factor_pair = _factor_pair(family.N, classification["factor"])
        if len(candidates) < max_stored_candidates:
            candidates.append(
                RecoveryCandidate(
                    relation=relation,
                    projected_relation=projected,
                    coefficients=coefficients,
                    cleared_embedding_squared_norm=sum(
                        value * value for value in cleared_vector
                    ),
                    relation_class=relation_class,
                    root_product=root_product,
                    factor_pair=relation_factor_pair,
                    discovery_node=nodes_visited,
                )
            )
        if relation_class == "L0" and adaptive_l0_suppression:
            if l0_lattice.add(relation):
                discovered += 1
        if relation_factor_pair is not None and factor_pair is None:
            factor_pair = relation_factor_pair
            if stop_on_factor:
                break

    memory_estimate = _state_memory_estimate(
        embedding,
        reduction,
        materialized_coefficients=materialized_count,
        coefficient_width=row_count,
        seen_relations=len(seen_keys),
        relation_dimension=embedding.dimension,
        candidate_count=len(candidates),
        l0_basis=l0_lattice.basis,
    )
    resources = RecoveryResources(
        nodes_generated=nodes_generated,
        nodes_visited=nodes_visited,
        duplicate_relation_nodes=duplicates,
        zero_projection_nodes=zero_projections,
        modular_relation_checks=modular_checks,
        verified_relations=verified,
        root_classifications=classifications,
        suppressed_l0_nodes=suppressed,
        l0_generators_discovered=discovered,
        reductions=reductions,
        reduction_backend=reduction.backend,
        runtime_seconds=perf_counter() - start,
        peak_memory_estimate_bytes=memory_estimate,
        memory_estimate_method=(
            "deterministic structural estimate: Python-int magnitude plus tuple/"
            "matrix/coefficient/relation storage; not process RSS"
        ),
        input_samples=embedding.sample_count - extra_samples_used,
        extra_samples_used=extra_samples_used,
        enumeration_rows=row_count,
        coefficient_bound=budget.coefficient_bound,
        max_support=max_support,
        node_budget=budget.max_nodes,
        node_budget_exhausted=exhausted,
    )
    interpretation = (
        "Exact bounded coefficient search over reduced rows; absence of a factor "
        "does not establish absence of L\\L0 vectors."
    )
    if adaptive_l0_suppression:
        interpretation += (
            " Suppression applies only to the exact integer span of verified L0 "
            "directions, never their rational span."
        )
    if root_blind:
        interpretation += (
            " Root-blind ablation: candidates are checked only for L membership; "
            "no stored root is evaluated and no L0/factor label is produced."
        )
    return RecoveryResult(
        strategy=strategy,
        embedding=embedding,
        reduction=reduction,
        candidates=tuple(candidates),
        factor_pair=factor_pair,
        discovered_l0_basis=l0_lattice.basis,
        resources=resources,
        interpretation=interpretation,
    )


