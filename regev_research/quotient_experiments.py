def _is_prime_exact(value: int) -> bool:
    if value < 2:
        return False
    if value == 2:
        return True
    if value % 2 == 0:
        return False
    return all(value % divisor for divisor in range(3, isqrt(value) + 1, 2))


@dataclass(frozen=True, slots=True)
class FrozenSemiprime:
    """Manifest-only factor certificate; never a sampler/selector input."""

    N: int
    p: int
    q: int
    split: str

    def __post_init__(self) -> None:
        if self.split not in ("development", "heldout"):
            raise ValueError("split must be development or heldout")
        if not self.p < self.q:
            raise ValueError("semiprime factors must be ordered and distinct")
        if not _is_prime_exact(self.p) or not _is_prime_exact(self.q):
            raise ValueError("manifest factors must both be prime")
        if self.N != self.p * self.q:
            raise ValueError("manifest N must equal p*q")


def _case(p: int, q: int, N: int, split: str) -> FrozenSemiprime:
    return FrozenSemiprime(N=N, p=p, q=q, split=split)


