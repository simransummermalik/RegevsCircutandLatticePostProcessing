@dataclass(frozen=True, slots=True)
class QuotientExperimentFreeze:
    """All precommitted parameters for the quotient-recovery study."""

    version: str = "quotient-recovery-freeze-v2-final-before-holdout"
    dimension: int = 3
    finite_modulus_D: int = 64
    gaussian_radius_R: int = 16
    finite_reconstruction_scale: Fraction = Fraction(13, 1)
    sample_counts: tuple[int, ...] = (7, 8, 9, 10, 11)
    master_seed: int = 2_026_071_203
    trials_per_case_model_count: int = 32
    exact_difference_point_budget: int = 2_500_000
    theorem_noise_safety: Fraction = Fraction(2, 1)
    readout_bit_flip_probability: Fraction = Fraction(1, 100)
    corrupt_shot_probability: Fraction = Fraction(1, 20)
    recovery_budget: RecoveryBudget = RecoveryBudget(
        enumeration_rows=6,
        coefficient_bound=2,
        max_support=3,
        max_nodes=50,
        max_materialized_nodes=1_000_000,
    )
    # A complete LDAR stage receives half of the common 50-visited-candidate
    # cap and may rerun once after U changes.  Thus it cannot exceed 50 visited
    # coefficient candidates at a fixed sample count.  Exact-norm ordering
    # materializes the declared finite search set before visiting it; generated
    # nodes, the extra LLL call, runtime, and memory are separately charged.
    ldar_stage_budget: RecoveryBudget = RecoveryBudget(
        enumeration_rows=6,
        coefficient_bound=2,
        max_support=3,
        max_nodes=25,
        max_materialized_nodes=1_000_000,
    )
    max_ldar_rounds_per_sample_count: int = 2
    max_augmented_deflation_deletions: int = 3
    bkz_block_size: int = 6
    rv_pool_sample_count: int = 11
    rv_target_sample_count: int = 7
    target_recovery_probability: Fraction = Fraction(4, 5)
    quotient_gap_log2_threshold: Fraction = Fraction(0, 1)
    bootstrap_resamples_at_N_level: int = 5_000
    predictor_diversity_relation_bound: int = 2
    quotient_relation_box_bound: int = 16

    def __post_init__(self) -> None:
        if self.dimension != 3:
            raise ValueError("the frozen study dimension is three")
        if self.finite_modulus_D <= 0 or self.finite_modulus_D & (self.finite_modulus_D - 1):
            raise ValueError("D must be a positive power of two")
        lower = 2 * sqrt(self.dimension) * self.gaussian_radius_R
        upper = 4 * sqrt(self.dimension) * self.gaussian_radius_R
        if not lower <= self.finite_modulus_D < upper:
            raise ValueError("D and R violate Regev's finite-register interval")
        if self.sample_counts != tuple(range(self.dimension + 4, self.dimension + 9)):
            raise ValueError("sample counts must be exactly d+4 through d+8")
        if self.finite_reconstruction_scale <= 0:
            raise ValueError("finite reconstruction scale must be positive")
        if self.exact_difference_point_budget < (2 * self.finite_modulus_D - 1) ** self.dimension:
            raise ValueError("exact evaluator budget is below the frozen D,d grid")
        for probability in (
            self.readout_bit_flip_probability,
            self.corrupt_shot_probability,
        ):
            if not 0 <= probability < 1:
                raise ValueError("surrogate probabilities must lie in [0,1)")
        if 2 * self.ldar_stage_budget.max_nodes != self.recovery_budget.max_nodes:
            raise ValueError("two LDAR rounds must match the common node cap")
        if self.max_ldar_rounds_per_sample_count != 2:
            raise ValueError("the final freeze permits exactly two LDAR rounds")
        if self.rv_pool_sample_count != max(self.sample_counts):
            raise ValueError("the RV finite comparator uses the full frozen pool")
        if self.rv_target_sample_count != min(self.sample_counts):
            raise ValueError("the RV finite comparator targets the initial sample count")
        if not 0 < self.target_recovery_probability < 1:
            raise ValueError("target recovery probability must lie in (0,1)")


FROZEN_QUOTIENT_EXPERIMENT = QuotientExperimentFreeze()


def deterministic_model_seed(
    case_index: int,
    model_label: str,
    sample_count: int,
    replicate: int,
    freeze: QuotientExperimentFreeze = FROZEN_QUOTIENT_EXPERIMENT,
) -> int:
    """Return a stable, non-hash-randomized seed for one frozen cell."""

    if model_label not in QUOTIENT_MODEL_LABELS:
        raise ValueError("unknown frozen model label")
    if sample_count not in freeze.sample_counts:
        raise ValueError("sample_count is outside the frozen schedule")
    if case_index < 0:
        raise ValueError("case_index must be nonnegative")
    if not 0 <= replicate < freeze.trials_per_case_model_count:
        raise ValueError("replicate is outside the frozen trial budget")
    model_index = QUOTIENT_MODEL_LABELS.index(model_label)
    count_index = freeze.sample_counts.index(sample_count)
    return (
        freeze.master_seed
        + 10_000_000 * case_index
        + 100_000 * model_index
        + 1_000 * count_index
        + replicate
    )


def _prime_stream_after_five():
    candidate = 7
    while True:
        if _is_prime_exact(candidate):
            yield candidate
        candidate += 2


def deterministic_small_prime_family(N: int, dimension: int = 3) -> RootedBaseFamily:
    """Select roots from ``[2,3,5]`` then deterministic primes using only GCDs.

    The function receives no factorization, orders, square roots, or planted
    relations.  A nonunit candidate is skipped; its GCD is not returned or
    used for anything other than the unit predicate.
    """

    N = int(N)
    dimension = int(dimension)
    if N <= 2 or dimension <= 0:
        raise ValueError("N must exceed two and dimension must be positive")
    selected: list[int] = []
    seen: set[int] = set()
    for candidate in (2, 3, 5):
        root = candidate % N
        if root not in seen and gcd(root, N) == 1:
            selected.append(root)
            seen.add(root)
            if len(selected) == dimension:
                return RootedBaseFamily.from_roots(N, selected)
    for candidate in _prime_stream_after_five():
        root = candidate % N
        if root in seen or gcd(root, N) != 1:
            continue
        selected.append(root)
        seen.add(root)
        if len(selected) == dimension:
            return RootedBaseFamily.from_roots(N, selected)
    raise AssertionError("unreachable prime stream exhaustion")


@dataclass(frozen=True, slots=True)
class ExactProbabilityModel:
    model_label: str
    family: RootedBaseFamily
    modulus: int
    radius: int | None
    probabilities: np.ndarray
    folded_kernel: np.ndarray
    amplitude_squared_normalization: float
    probability_mass_error: float

    def __post_init__(self) -> None:
        expected_shape = (self.modulus,) * len(self.family.pairs)
        if self.probabilities.shape != expected_shape:
            raise ValueError("probability tensor has the wrong shape")
        if self.folded_kernel.shape != expected_shape:
            raise ValueError("folded kernel has the wrong shape")
        if not np.all(np.isfinite(self.probabilities)) or np.any(self.probabilities < 0):
            raise ValueError("probabilities must be finite and nonnegative")
        if abs(float(self.probabilities.sum()) - 1.0) > 1e-12:
            raise ValueError("exact probability model is not normalized")
        self.probabilities.setflags(write=False)
        self.folded_kernel.setflags(write=False)


@dataclass(frozen=True, slots=True)
class FrozenSampleBatch:
    model_label: str
    samples: tuple[tuple[int, ...], ...]
    modulus: int
    reconstruction_scale: Fraction
    seed: int
    sample_count: int
    latent_samples: tuple[tuple[int, ...], ...] | None
    metadata: tuple[tuple[str, object], ...]

    def metadata_dict(self) -> Mapping[str, object]:
        return dict(self.metadata)


def _validate_family_and_count(
    family: RootedBaseFamily,
    sample_count: int | None,
    freeze: QuotientExperimentFreeze,
) -> int | None:
    if not isinstance(family, RootedBaseFamily):
        raise TypeError("sampling requires a RootedBaseFamily")
    if len(family.pairs) != freeze.dimension:
        raise ValueError("rooted family dimension does not match the freeze")
    if sample_count is not None and sample_count not in freeze.sample_counts:
        raise ValueError("sample_count is outside the frozen schedule")
    return sample_count


@lru_cache(maxsize=None)
def exact_uniform_hard_box_model(
    family: RootedBaseFamily,
    freeze: QuotientExperimentFreeze = FROZEN_QUOTIENT_EXPERIMENT,
) -> ExactProbabilityModel:
    _validate_family_and_count(family, None, freeze)
    probabilities, kernel, normalization = exact_weighted_fourier_distribution(
        family.N,
        family.bases,
        freeze.finite_modulus_D,
        uniform_amplitudes(freeze.finite_modulus_D),
        max_difference_points=freeze.exact_difference_point_budget,
    )
    return ExactProbabilityModel(
        model_label=UNIFORM_HARD_BOX_MODEL,
        family=family,
        modulus=freeze.finite_modulus_D,
        radius=None,
        probabilities=probabilities,
        folded_kernel=kernel,
        amplitude_squared_normalization=normalization,
        probability_mass_error=abs(float(probabilities.sum()) - 1.0),
    )


@lru_cache(maxsize=None)
def exact_finite_discrete_gaussian_model(
    family: RootedBaseFamily,
    freeze: QuotientExperimentFreeze = FROZEN_QUOTIENT_EXPERIMENT,
) -> ExactProbabilityModel:
    _validate_family_and_count(family, None, freeze)
    probabilities, kernel, normalization = exact_regev_gaussian_distribution(
        family.N,
        family.bases,
        freeze.finite_modulus_D,
        freeze.gaussian_radius_R,
        max_difference_points=freeze.exact_difference_point_budget,
    )
    return ExactProbabilityModel(
        model_label=FINITE_GAUSSIAN_MODEL,
        family=family,
        modulus=freeze.finite_modulus_D,
        radius=freeze.gaussian_radius_R,
        probabilities=probabilities,
        folded_kernel=kernel,
        amplitude_squared_normalization=normalization,
        probability_mass_error=abs(float(probabilities.sum()) - 1.0),
    )


def _sample_probability_model(
    model: ExactProbabilityModel,
    sample_count: int,
    seed: int,
    freeze: QuotientExperimentFreeze,
) -> FrozenSampleBatch:
    _validate_family_and_count(model.family, sample_count, freeze)
    sampled = sample_exact_distribution(model.probabilities, sample_count, seed)
    rows = tuple(tuple(int(value) for value in row) for row in sampled)
    return FrozenSampleBatch(
        model_label=model.model_label,
        samples=rows,
        modulus=model.modulus,
        reconstruction_scale=freeze.finite_reconstruction_scale,
        seed=int(seed),
        sample_count=sample_count,
        latent_samples=None,
        metadata=(
            ("generator", "exact finite probability tensor"),
            ("probability_mass_error", model.probability_mass_error),
        ),
    )


def sample_exact_uniform_hard_box(
    family: RootedBaseFamily,
    sample_count: int,
    seed: int,
    freeze: QuotientExperimentFreeze = FROZEN_QUOTIENT_EXPERIMENT,
) -> FrozenSampleBatch:
    return _sample_probability_model(
        exact_uniform_hard_box_model(family, freeze), sample_count, seed, freeze
    )


def sample_exact_finite_discrete_gaussian(
    family: RootedBaseFamily,
    sample_count: int,
    seed: int,
    freeze: QuotientExperimentFreeze = FROZEN_QUOTIENT_EXPERIMENT,
) -> FrozenSampleBatch:
    return _sample_probability_model(
        exact_finite_discrete_gaussian_model(family, freeze),
        sample_count,
        seed,
        freeze,
    )


def sample_theorem_consistent_noisy_dual(
    family: RootedBaseFamily,
    sample_count: int,
    seed: int,
    freeze: QuotientExperimentFreeze = FROZEN_QUOTIENT_EXPERIMENT,
) -> FrozenSampleBatch:
    _validate_family_and_count(family, sample_count, freeze)
    generated = synthetic_noisy_dual_samples(
        family,
        seed=int(seed),
        sample_count=sample_count,
        safety=float(freeze.theorem_noise_safety),
    )
    if not generated.theorem_sufficient_inequality:
        raise ArithmeticError("theoretical noisy-dual batch violated its sufficient inequality")
    return FrozenSampleBatch(
        model_label=THEOREM_NOISY_DUAL_MODEL,
        samples=generated.samples,
        modulus=generated.modulus,
        reconstruction_scale=Fraction(generated.scale, 1),
        seed=int(seed),
        sample_count=sample_count,
        latent_samples=None,
        metadata=(
            ("generator", "factor-blind uniform dual coset plus bounded torus noise"),
            ("generator_oracle_withheld_from_recovery", True),
            ("noise_bound", generated.noise_bound),
            ("maximum_realized_torus_error", generated.maximum_realized_torus_error),
            ("theorem_sufficient_inequality", True),
            ("relation_norm_bound_T", generated.relation_norm_bound_T),
        ),
    )


def sample_circuit_derived_readout_corruption_surrogate(
    family: RootedBaseFamily,
    sample_count: int,
    seed: int,
    freeze: QuotientExperimentFreeze = FROZEN_QUOTIENT_EXPERIMENT,
) -> FrozenSampleBatch:
    """Apply declared classical corruption to exact notebook-circuit samples."""

    latent_batch = sample_exact_uniform_hard_box(family, sample_count, seed, freeze)
    values = np.asarray(latent_batch.samples, dtype=np.int64).copy()
    bit_width = freeze.finite_modulus_D.bit_length() - 1
    rng = np.random.default_rng(int(seed) + 7_919)
    flip_probability = float(freeze.readout_bit_flip_probability)
    for bit in range(bit_width):
        flips = rng.random(values.shape) < flip_probability
        values ^= flips.astype(np.int64) << bit
    corrupt = rng.random(sample_count) < float(freeze.corrupt_shot_probability)
    if np.any(corrupt):
        values[corrupt] = rng.integers(
            0,
            freeze.finite_modulus_D,
            size=(int(np.count_nonzero(corrupt)), freeze.dimension),
        )
    rows = tuple(tuple(int(value) for value in row) for row in values)
    return FrozenSampleBatch(
        model_label=CIRCUIT_SURROGATE_MODEL,
        samples=rows,
        modulus=freeze.finite_modulus_D,
        reconstruction_scale=freeze.finite_reconstruction_scale,
        seed=int(seed),
        sample_count=sample_count,
        latent_samples=latent_batch.samples,
        metadata=(
            ("generator", "explicit circuit-derived classical corruption surrogate"),
            ("latent_model", UNIFORM_HARD_BOX_MODEL),
            ("readout_bit_flip_probability", freeze.readout_bit_flip_probability),
            ("corrupt_shot_probability", freeze.corrupt_shot_probability),
            ("is_gate_level_or_hardware_noise_model", False),
        ),
    )


