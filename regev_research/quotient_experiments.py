"""Frozen infrastructure for quotient-recovery experiments.

This module intentionally contains no held-out execution loop.  It freezes the
development and held-out semiprime manifests, factor-blind rooted-family rule,
model labels, seeds, and budgets needed by a later quotient-recovery study.

Four sampling models are kept distinct:

``A_exact_uniform_hard_box``
    The exact finite output law of the supplied notebook's uniform exponent
    box, evaluated by the autocorrelation/character identity in ``redteam``.

``B_exact_finite_discrete_gaussian``
    The exact finite output law for Regev's truncated ``rho_R`` amplitudes.

``C_theorem_consistent_noisy_dual``
    Factor-blind oracle-side uniform dual-coset samples with a certified torus
    noise bound, generated by ``dual.synthetic_noisy_dual_samples``.  Its
    oracle is deliberately discarded before returning the recovery input.

``D_circuit_derived_readout_corruption_surrogate``
    A plainly labelled surrogate: draw from model A, then apply independent
    classical readout-bit flips and whole-shot uniform corruption.  It is not
    a gate-level, device, or calibrated hardware noise model.

Known semiprime factors exist only in the frozen manifest so products and
strata can be audited.  Family selection and all samplers accept ``N`` or a
``RootedBaseFamily`` and never receive those factors.
"""

