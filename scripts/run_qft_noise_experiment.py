"""Run the frozen finite-QFT/noise validation study.

The exact fiber calculations are deliberately small-state: they validate the
roots-of-unity matrix model and the adaptive cutoff on development inputs and
six representative held-out inputs.  For all 24 held-out semiprimes we also
record the factor-blind analytic truncation bound.  Model C is generated by
the existing theorem-consistent noisy-dual oracle, with its oracle hidden from
the reported recovery-side fields.  Model D measures only feasible N=15
Qiskit resources and is explicitly not a hardware experiment.
"""

from __future__ import annotations

import csv
import json
import sys
from pathlib import Path
from time import perf_counter
from math import sqrt

import numpy as np
from qiskit import transpile
from qiskit_aer import AerSimulator

ROOT = Path(__file__).resolve().parents[1]
if str(ROOT) not in sys.path:
    sys.path.insert(0, str(ROOT))

from regev_research.circuits import build_arbitrary_base_circuit
from regev_research.core import RootedBaseFamily, sample_distribution
from regev_research.dual import synthetic_noisy_dual_samples
from regev_research.dual import exact_relation_lattice_hnf
from regev_research.lattice import regev_lattice_postprocess
from regev_research.qft_noise import (
    fiber_fourier_distribution,
    fiber_qft_tv_distance,
    grid_quantization_bound,
    qft_gate_counts,
    qft_tv_bound,
    select_qft_cutoff,
    weighted_fiber_fourier_distribution,
    weighted_fiber_qft_tv_distance,
)
from regev_research.redteam import regev_gaussian_amplitudes


OUT = ROOT / "results" / "qft_noise"
DEV = [77, 91, 143]
HELDOUT = [
    2279, 2419, 2491, 2501, 2537, 2623, 2747, 2773, 2867, 2881,
    2911, 2993, 3053, 3127, 3139, 3149, 3233, 3337, 3431, 3551,
    3599, 3763, 3977, 3953,
]
ROOTS = (2, 3, 4)
M = 8
RADIUS = 4.0
SAMPLE_COUNT = 12
ENDPOINT_SAMPLE_COUNT = 7
ENDPOINT_REPLICATES = 32
MASTER_SEED = 2026071107
LOSS_BUDGET = 0.05
ANALYTIC_CUTOFFS = tuple(range(3))


def _rows_to_csv(path: Path, rows: list[dict]) -> None:
    if not rows:
        return
    keys = sorted({key for row in rows for key in row})
    with path.open("w", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=keys)
        writer.writeheader()
        writer.writerows(rows)


def _wilson_interval(successes: int, trials: int, z: float = 1.96) -> tuple[float, float]:
    """95% Wilson interval, retaining N as the primary paired unit."""

    if trials <= 0 or not 0 <= successes <= trials:
        raise ValueError("invalid binomial counts")
    p = successes / trials
    denominator = 1.0 + z * z / trials
    center = (p + z * z / (2 * trials)) / denominator
    radius = z * sqrt(p * (1 - p) / trials + z * z / (4 * trials * trials)) / denominator
    return max(0.0, center - radius), min(1.0, center + radius)


def _fiber_row(N: int, model: str, cutoff: int, split: str, exact: bool) -> dict:
    family = RootedBaseFamily.from_roots(N, ROOTS)
    start = perf_counter()
    if model == "A_uniform_hard_box":
        probabilities = fiber_fourier_distribution(N, family.bases, M, cutoff=cutoff)
    elif model == "B_exact_finite_discrete_gaussian":
        amplitudes = regev_gaussian_amplitudes(M, RADIUS)
        probabilities = weighted_fiber_fourier_distribution(
            N, family.bases, M, amplitudes, cutoff=cutoff
        )
    else:
        raise ValueError(model)
    tv = (
        fiber_qft_tv_distance(N, family.bases, M, cutoff)
        if model == "A_uniform_hard_box"
        else weighted_fiber_qft_tv_distance(
            N, family.bases, M, regev_gaussian_amplitudes(M, RADIUS), cutoff
        )
    )
    counts = qft_gate_counts(len(ROOTS), int(np.log2(M)), cutoff)
    return {
        "N": N,
        "split": split,
        "model": model,
        "cutoff": cutoff,
        "M": M,
        "d": len(ROOTS),
        "sample_count": SAMPLE_COUNT,
        "exact_fiber_evaluation": exact,
        "tv_to_exact_same_model": tv,
        "m_shot_tv_union_bound": min(1.0, SAMPLE_COUNT * tv),
        "qft_operator_bound": qft_tv_bound(len(ROOTS), int(np.log2(M)), cutoff),
        "controlled_phase_gates": counts["controlled_phase"],
        "exact_controlled_phase_gates": counts["exact_controlled_phase"],
        "elapsed_seconds": perf_counter() - start,
        "seed": MASTER_SEED,
    }


def main() -> None:
    OUT.mkdir(parents=True, exist_ok=True)
    rows: list[dict] = []
    # Exact finite fiber laws: all development inputs and six frozen held-out
    # inputs.  The other held-out rows are certified analytically below.
    for split, inputs in (("development", DEV), ("heldout_exact", HELDOUT[:6])):
        for N in inputs:
            for model in ("A_uniform_hard_box", "B_exact_finite_discrete_gaussian"):
                for cutoff in ANALYTIC_CUTOFFS:
                    rows.append(_fiber_row(N, model, cutoff, split, True))
    _rows_to_csv(OUT / "fiber_rows.csv", rows)

    analytic_rows: list[dict] = []
    for N in DEV + HELDOUT:
        for model in ("A_uniform_hard_box", "B_exact_finite_discrete_gaussian"):
            choice = select_qft_cutoff(len(ROOTS), M, SAMPLE_COUNT, LOSS_BUDGET)
            for cutoff in ANALYTIC_CUTOFFS:
                analytic_rows.append({
                    "N": N,
                    "split": "development" if N in DEV else "heldout",
                    "model": model,
                    "cutoff": cutoff,
                    "analytic_per_shot_tv_bound": qft_tv_bound(len(ROOTS), int(np.log2(M)), cutoff),
                    "analytic_m_shot_loss_bound": min(1.0, SAMPLE_COUNT * qft_tv_bound(len(ROOTS), int(np.log2(M)), cutoff)),
                    "factor_blind_selected_cutoff": choice.cutoff,
                    "factor_blind_cutoff_certified": choice.certified,
                    "seed": MASTER_SEED,
                })
    _rows_to_csv(OUT / "analytic_rows.csv", analytic_rows)

    # Model C: theorem-consistent samples and the exact embedding noise budget.
    model_c_rows: list[dict] = []
    for split, inputs in (("development", DEV), ("heldout", HELDOUT)):
        for index, N in enumerate(inputs):
            family = RootedBaseFamily.from_roots(N, ROOTS)
            seed = MASTER_SEED + index + (0 if split == "development" else 1000)
            batch = synthetic_noisy_dual_samples(
                family, seed=seed, sample_count=SAMPLE_COUNT, relation_norm_bound_T=4, safety=2.0
            )
            model_c_rows.append({
                "N": N,
                "split": split,
                "model": "C_theorem_noisy_dual",
                "sample_count": SAMPLE_COUNT,
                "scale": batch.scale,
                "modulus": batch.modulus,
                "noise_bound": batch.noise_bound,
                "maximum_realized_torus_error": batch.maximum_realized_torus_error,
                "grid_bound_for_d_M": grid_quantization_bound(len(ROOTS), batch.modulus),
                "theorem_sufficient_inequality": batch.theorem_sufficient_inequality,
                "seed": seed,
                "oracle_hidden_from_recovery": True,
            })
    _rows_to_csv(OUT / "model_c_rows.csv", model_c_rows)

    # Model D: actual Qiskit circuit-resource check at the smallest nontrivial
    # instance.  It does not claim a hardware noise calibration.
    circuit_rows: list[dict] = []
    N, roots, modulus = 15, (2, 4), 4
    for cutoff in (0, 1):
        circuit = build_arbitrary_base_circuit(
            N, [root * root % N for root in roots], modulus,
            inverse_qft=True, measure=True, qft_cutoff=cutoff
        )
        compiled = transpile(
            circuit, AerSimulator(), optimization_level=1, seed_transpiler=MASTER_SEED
        )
        circuit_rows.append({
            "N": N,
            "model": "D_qiskit_small_instance",
            "cutoff": cutoff,
            "M": modulus,
            "d": len(roots),
            "qubits": circuit.num_qubits,
            "logical_depth": circuit.depth(),
            "compiled_depth": compiled.depth(),
            "logical_ops": json.dumps({str(k): int(v) for k, v in circuit.count_ops().items()}, sort_keys=True),
            "compiled_ops": json.dumps({str(k): int(v) for k, v in compiled.count_ops().items()}, sort_keys=True),
            "seed": MASTER_SEED,
            "hardware_noise_model": False,
        })
    _rows_to_csv(OUT / "qiskit_rows.csv", circuit_rows)

    # Actual samples -> augmented lattice -> LLL -> stored-root factor endpoint
    # on three small semiprimes.  This is a feasibility endpoint, not a claim
    # about the large held-out suite; factors are consulted only after the
    # factor-blind postprocessor returns a pair for validation.
    endpoint_rows: list[dict] = []
    endpoint_inputs = [35, 77, 143]
    for N in endpoint_inputs:
        family = RootedBaseFamily.from_roots(N, ROOTS)
        for model in ("A_uniform_hard_box", "B_exact_finite_discrete_gaussian"):
            amplitudes = regev_gaussian_amplitudes(M, RADIUS)
            for cutoff in ANALYTIC_CUTOFFS:
                successes = 0
                runtimes: list[float] = []
                class_counts: dict[str, int] = {}
                for replicate in range(ENDPOINT_REPLICATES):
                    if model == "A_uniform_hard_box":
                        probabilities = fiber_fourier_distribution(
                            N, family.bases, M, cutoff=cutoff
                        )
                    else:
                        probabilities = weighted_fiber_fourier_distribution(
                            N, family.bases, M, amplitudes, cutoff=cutoff
                        )
                    seed = MASTER_SEED + 20_000 + N + replicate
                    samples = sample_distribution(probabilities, ENDPOINT_SAMPLE_COUNT, seed)
                    started = perf_counter()
                    result = regev_lattice_postprocess(
                        family,
                        samples,
                        M,
                        claim_norm_bound=4,
                        scale=4,
                    )
                    runtimes.append(perf_counter() - started)
                    if result.factor_pair is not None:
                        successes += 1
                    for candidate in result.candidates:
                        class_counts[candidate.relation_class] = class_counts.get(candidate.relation_class, 0) + 1
                endpoint_rows.append({
                    "N": N,
                    "model": model,
                    "cutoff": cutoff,
                    "replicates": ENDPOINT_REPLICATES,
                    "sample_count": ENDPOINT_SAMPLE_COUNT,
                    "factor_successes": successes,
                    "factor_recovery_probability": successes / ENDPOINT_REPLICATES,
                    "ci95_low": _wilson_interval(successes, ENDPOINT_REPLICATES)[0],
                    "ci95_high": _wilson_interval(successes, ENDPOINT_REPLICATES)[1],
                    "mean_lll_seconds": float(np.mean(runtimes)),
                    "class_counts": json.dumps(class_counts, sort_keys=True),
                    "seed_base": MASTER_SEED + 20_000 + N,
                    "factors_used_for_tuning": False,
                })
        oracle = exact_relation_lattice_hnf(N, family.bases)
        c_successes = 0
        c_runtimes: list[float] = []
        c_classes: dict[str, int] = {}
        for replicate in range(ENDPOINT_REPLICATES):
            batch = synthetic_noisy_dual_samples(
                family,
                seed=MASTER_SEED + 30_000 + N + replicate,
                sample_count=ENDPOINT_SAMPLE_COUNT,
                relation_norm_bound_T=4,
                safety=2.0,
                oracle=oracle,
            )
            started = perf_counter()
            result = regev_lattice_postprocess(
                family,
                batch.samples,
                batch.modulus,
                claim_norm_bound=4,
                scale=batch.scale,
            )
            c_runtimes.append(perf_counter() - started)
            c_successes += int(result.factor_pair is not None)
            for candidate in result.candidates:
                c_classes[candidate.relation_class] = c_classes.get(candidate.relation_class, 0) + 1
        endpoint_rows.append({
            "N": N,
            "model": "C_theorem_noisy_dual",
            "cutoff": "not_applicable",
            "replicates": ENDPOINT_REPLICATES,
            "sample_count": ENDPOINT_SAMPLE_COUNT,
            "factor_successes": c_successes,
            "factor_recovery_probability": c_successes / ENDPOINT_REPLICATES,
            "ci95_low": _wilson_interval(c_successes, ENDPOINT_REPLICATES)[0],
            "ci95_high": _wilson_interval(c_successes, ENDPOINT_REPLICATES)[1],
            "mean_lll_seconds": float(np.mean(c_runtimes)),
            "class_counts": json.dumps(c_classes, sort_keys=True),
            "seed_base": MASTER_SEED + 30_000 + N,
            "factors_used_for_tuning": False,
        })
    _rows_to_csv(OUT / "endpoint_rows.csv", endpoint_rows)

    # Compact figures used by the report.  They show the exact finite-fiber
    # validation, not a fitted recovery predictor.
    import matplotlib
    matplotlib.use("Agg")
    import matplotlib.pyplot as plt
    import pandas as pd

    fiber_frame = pd.DataFrame(rows)
    figure, axis = plt.subplots(figsize=(6.5, 4.0))
    for model, color in (("A_uniform_hard_box", "#3366cc"), ("B_exact_finite_discrete_gaussian", "#dc3912")):
        subset = fiber_frame[(fiber_frame["model"] == model) & (fiber_frame["split"] == "development")]
        grouped = subset.groupby("cutoff")["tv_to_exact_same_model"].agg(["mean", "std"])
        axis.errorbar(grouped.index, grouped["mean"], yerr=grouped["std"].fillna(0), marker="o", capsize=3, label=model.split("_")[0], color=color)
    axis.set_xlabel("retained QFT phase separation cutoff t")
    axis.set_ylabel("TV distance to exact finite fiber law")
    axis.set_title("Finite d=3, M=8 QFT truncation error (development)")
    axis.legend()
    figure.tight_layout()
    figure.savefig(OUT / "qft_tv_vs_cutoff.png", dpi=180)
    plt.close(figure)

    endpoint_frame = pd.DataFrame(endpoint_rows)
    endpoint_frame = endpoint_frame[endpoint_frame["model"] != "C_theorem_noisy_dual"]
    figure, axis = plt.subplots(figsize=(7.0, 4.0))
    for model, marker in (("A_uniform_hard_box", "o"), ("B_exact_finite_discrete_gaussian", "s")):
        subset = endpoint_frame[endpoint_frame["model"] == model]
        for N in endpoint_inputs:
            points = subset[subset["N"] == N]
            axis.plot(points["cutoff"], points["factor_recovery_probability"], marker=marker, label=f"{model.split('_')[0]}, N={N}")
    axis.set_xlabel("retained QFT phase separation cutoff t")
    axis.set_ylabel("factor recovery probability (32 replicates)")
    axis.set_ylim(0, 1.05)
    axis.legend(fontsize=7, ncol=2)
    axis.set_title("QFT precision versus augmented-lattice factor endpoint")
    figure.tight_layout()
    figure.savefig(OUT / "endpoint_success_vs_cutoff.png", dpi=180)
    plt.close(figure)

    config = {
        "freeze_version": "qft-noise-finite-parameter-v1",
        "development_N": DEV,
        "heldout_N": HELDOUT,
        "roots": ROOTS,
        "dimension": len(ROOTS),
        "M": M,
        "gaussian_radius_R": RADIUS,
        "sample_count": SAMPLE_COUNT,
        "master_seed": MASTER_SEED,
        "loss_budget": LOSS_BUDGET,
        "exact_fiber_evaluation_inputs": DEV + HELDOUT[:6],
        "analytic_bound_inputs": DEV + HELDOUT,
        "model_C_relation_norm_bound_T": 4,
        "model_C_safety": 2.0,
        "selection_is_factor_blind": True,
        "factors_used_for_selection_or_tuning": False,
        "QFT_bound_status": "worst-case hybrid bound; exact fiber TV is a validation metric",
        "D_status": "Qiskit resource check only; not a hardware noise experiment",
        "endpoint_inputs": endpoint_inputs,
        "endpoint_replicates": ENDPOINT_REPLICATES,
        "endpoint_sample_count": ENDPOINT_SAMPLE_COUNT,
        "endpoint_scale": 4,
        "endpoint_claim_norm_bound": 4,
        "lattice_dimension": len(ROOTS) + ENDPOINT_SAMPLE_COUNT,
        "enumeration_nodes": 0,
        "bkz_used": False,
        "memory_accounting": "raw sample matrices and exact 10x10 integer embeddings; peak process memory not claimed as a hardware resource",
    }
    (OUT / "configuration.json").write_text(json.dumps(config, indent=2, sort_keys=True))
    print(json.dumps({"output": str(OUT), "fiber_rows": len(rows), "analytic_rows": len(analytic_rows), "model_C_rows": len(model_c_rows), "qiskit_rows": len(circuit_rows)}, indent=2))


if __name__ == "__main__":
    main()
